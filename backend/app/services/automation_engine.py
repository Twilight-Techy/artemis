"""
Automation Engine (AAL Evaluator)
─────────────────────────────────
Parses and evaluates Artemis Automation Language (AAL) rules.
Example: WHEN temperature > 28 IF somebody_home == true THEN turn on the fan.
"""

from typing import Dict, Any
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy import select
from app.models import Automation, User
